#include "libft.h"

char	*ft_strrchr(char *s, int c)
{
	size_t			len;

	len = ft_strlen(s);
	while (len)
	{
		if (s[len] == (char)c)
			return (s + len);
		len--;
	}
	if (s[0] == (char)c)
		return (s);
	return (0);
}
