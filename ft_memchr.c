#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	char	*mem;
	char	byte;

	mem = (char *)s;
	byte = c;
	if (n)
	{
		while (n--)
		{
			if (*mem == byte)
				return (mem);
			mem++;
		}
	}
	return (NULL);
}
