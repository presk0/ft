#include "libft.h"

void	*ft_memcpy(void *dst, const void *src, size_t len)
{
	char	*d;
	char	*s;

	d = dst;
	s = (void *)src;
	if (dst == src)
		return (dst);
	while (len--)
		*d++ = *s++;
	return (dst);
}
