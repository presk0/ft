#include "libft.h"

int	ft_lstsize(t_list *lst)
{
	unsigned int	len;

	if (lst == NULL)
		return (0);
	len = 1;
	while (lst->next != NULL)
	{
		lst = lst->next;
		len++;
	}
	return (len);
}
